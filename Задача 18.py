import random

# Ввод данных пользователем
N = int(input("Введите натуральное число N (количество элементов в массиве): "))
X = int(input("Введите число X для проверки: "))

# Заполнение массива случайными натуральными числами от 1 до N
A = [random.randint(1, N) for _ in range(N)]

# Вывод заполненного массива
print("Сгенерированный массив: ", A)

# Нахождение ближайшего элемента к X
closest_element = min(A, key=lambda element: (abs(element - X), element))

# Вывод результата
print(f"Ближайший к числу {X} элемент в массиве: {closest_element}")